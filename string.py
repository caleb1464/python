text = "hello"
text1 = "THERE"

print(text)

# Accessing an element in a string
print(text[1])

# Modifying a string
print(text.upper())
print(text1.lower())

# Size/length of a string
print(len(text))

# String Concatenation - Combining strings
print(text + " " + text1)

# Reassigning a string
text = "Good afternoon"
print(text)

# how to delete a string
text1 = ""
print(text1)

